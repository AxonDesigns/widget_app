class _AppState extends State<App> {
  bool get _usesRouter =>
      widget.routerDelegate != null || widget.routerConfig != null;

  Iterable<LocalizationsDelegate<dynamic>> get _localizationsDelegates {
    return <LocalizationsDelegate<dynamic>>[
      if (widget.localizationsDelegates != null)
        ...widget.localizationsDelegates!,
    ];
  }

  TextStyle get _textStyle =>
      widget.textStyle ??
      const TextStyle(color: Color.fromARGB(255, 255, 255, 255));
  Color get _color => widget.color ?? const Color.fromARGB(255, 51, 131, 250);

  Widget _buildWidgetsApp(BuildContext context) {
    if (_usesRouter) {
      return WidgetsApp.router(
        key: GlobalObjectKey(this),
        routeInformationParser: widget.routeInformationParser,
        routeInformationProvider: widget.routeInformationProvider,
        routerConfig: widget.routerConfig,
        routerDelegate: widget.routerDelegate,
        backButtonDispatcher: widget.backButtonDispatcher,
        onNavigationNotification: widget.onNavigationNotification,
        title: widget.title,
        builder:
            widget.builder ?? (context, child) => child ?? const SizedBox(),
        onGenerateTitle: widget.onGenerateTitle,
        textStyle: widget.textStyle ?? _textStyle,
        color: _color,
        locale: widget.locale,
        localizationsDelegates: _localizationsDelegates,
        localeListResolutionCallback: widget.localeListResolutionCallback,
        localeResolutionCallback: widget.localeResolutionCallback,
        supportedLocales: widget.supportedLocales,
        showPerformanceOverlay: widget.showPerformanceOverlay,
        showSemanticsDebugger: widget.showSemanticsDebugger,
        debugShowCheckedModeBanner: widget.debugShowCheckedModeBanner,
        inspectorSelectButtonBuilder: widget.inspectorSelectButtonBuilder,
        shortcuts: widget.shortcuts,
        actions: widget.actions,
        restorationScopeId: widget.restorationScopeId,
      );
    }

    return WidgetsApp(
      key: GlobalObjectKey(this),
      title: widget.title,
      routes: widget.routes!,
      initialRoute: widget.initialRoute,
      pageRouteBuilder: <T>(settings, builder) =>
          GenericPageRoute(settings: settings, builder: builder),
      onGenerateTitle: widget.onGenerateTitle,
      onGenerateInitialRoutes: widget.onGenerateInitialRoutes,
      onGenerateRoute: widget.onGenerateRoute,
      onUnknownRoute: widget.onUnknownRoute,
      onNavigationNotification: widget.onNavigationNotification,
      navigatorKey: widget.navigatorKey,
      home: widget.home,
      builder: widget.builder ?? (context, child) => child ?? const SizedBox(),
      textStyle: widget.textStyle ?? _textStyle,
      color: _color,
      locale: widget.locale,
      localizationsDelegates: _localizationsDelegates,
      localeListResolutionCallback: widget.localeListResolutionCallback,
      localeResolutionCallback: widget.localeResolutionCallback,
      supportedLocales: widget.supportedLocales,
      showPerformanceOverlay: widget.showPerformanceOverlay,
      showSemanticsDebugger: widget.showSemanticsDebugger,
      debugShowCheckedModeBanner: widget.debugShowCheckedModeBanner,
      inspectorSelectButtonBuilder: widget.inspectorSelectButtonBuilder,
      shortcuts: widget.shortcuts,
      actions: widget.actions,
      restorationScopeId: widget.restorationScopeId,
    );
  }

  @override
  Widget build(BuildContext context) {
    return Focus(
      canRequestFocus: false,
      onKeyEvent: (node, event) {
        // INFO: Useful for testing, allows you to use the keys for anything.
        return KeyEventResult.ignored;
      },
      child: ScrollConfiguration(
        behavior: widget.scrollBehavior ?? const ScrollBehavior(),
        child: _buildWidgetsApp(context),
      ),
    );
  }
}
